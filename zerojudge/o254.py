'''
內容
板橋高中的暑假裡，有一個學妹找到臨末，由於臨末時常欺負(X) 照顧(O)他，所以她決定要變得比臨末還強，這樣她就可以欺負(X) 照顧(O)臨末了。

給你目前學妹跟臨末分別的強度，請你不斷加強學妹的強度，使學妹的強度比臨末的強度高。

PS. 搞不好學妹已經比臨末強了喔
輸入說明
輸入包含兩個正整數A, B，A代表學妹目前的強度 B代表臨末目前的強度

1<=A, B<=10^9

輸出說明
請輸出學妹至少要將強度提升至多少，才能比臨末還要強。

範例輸入 #1
3 500
範例輸出 #1
501
範例輸入 #2
1 1000000000
範例輸出 #2
1000000001
範例輸入 #3
100 7
範例輸出 #3
100
提示 ：
範例3當中，學妹本來就吊打臨末，所以強度不需要加強了。

標籤:
板中高一練習題
'''
a, b = input().split()
if int(a) > int(b): print(int(a))
else: print(int(b) + 1)